function RichText({ text }: { text: string }) {
  const lines = text.split("\n")
  const elements: any[] = []
  let inList = false
  let listItems: any[] = []
  let ulCount = 0

  const flushList = () => {
    if (inList && listItems.length > 0) {
      elements.push(
        <ul key={`ul-${ulCount++}`} className="list-disc list-outside ml-5 space-y-2 text-white/80 marker:text-[#BFF202] mb-4">
          {listItems}
        </ul>
      )
      listItems = []
      inList = false
    }
  }

  lines.forEach((line, i) => {
    if (!line.trim()) {
      flushList()
      return
    }

    const isListItem = line.trim().startsWith("- ")
    
    if (isListItem) {
      inList = true
      const content = line.replace("- ", "").trim()
      const parts = content.split(/\*\*(.*?)\*\*/g)
      const rendered = parts.map((part, j) =>
        j % 2 === 1 ? <strong key={j} className="font-bold text-[#BFF202]">{part}</strong> : <span key={j}>{part.replace(/'/g, "'")}</span>
      )
      listItems.push(<li key={`li-${i}`} className="pl-1 leading-relaxed">{rendered}</li>)
    } else {
      flushList()
      const parts = line.split(/\*\*(.*?)\*\*/g)
      const rendered = parts.map((part, j) =>
        j % 2 === 1 ? <strong key={j} className="font-bold text-[#BFF202]">{part}</strong> : <span key={j}>{part.replace(/'/g, "'")}</span>
      )
      elements.push(
        <p key={`p-${i}`} className="leading-relaxed mb-4 last:mb-0 text-white/80">
          {rendered}
        </p>
      )
    }
  })
  
  flushList()

  return <div className="text-[15px] sm:text-base">{elements}</div>
}

